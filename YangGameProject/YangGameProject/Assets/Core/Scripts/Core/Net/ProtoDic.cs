        public static Type GetProtoTypeByProtoId(int protoId)
        {
            foreach (KeyValuePair<int, Type> kvp in protoIdWithType)
            {
                if (kvp.Key == protoId)
                return kvp.Value;
            }
            UnityEngine.Log.Error("协议存在问题,协议ID=" + protoId);
            return null;
        }

        public static int GetProtoIdByProtoType(Type type)
        {
            foreach (KeyValuePair<int, Type> kvp in protoIdWithType)
            {
                if (kvp.Value == type)
                return kvp.Key;
            }
            UnityEngine.Log.Error("协议存在问题,协议TYPE=" + type);
            return 0;
        }

        public static bool ContainProtoId(int protoId)
        {
            if(protoIdWithType.ContainsKey(protoId))
            {
                return true;
            }
            return false;
        }

        public static bool ContainProtoType(Type type)
        {
            if(protoIdWithType.ContainsValue(type))
            {
                return true;
            }
            return false;
        }
